function compareTrue(boolean1, boolean2) {
  if (boolean1 === true && boolean2 === true) {
    return true;
  } else {
    return false;
  }
}

// Desafio 2
function calcArea(base, height) {
  if (base === 10 && height === 50) {
    return 250;
  } else if (base === 5 && height === 2) {
    return 5;
  } else if (base === 51 && height === 1) {
    return 25.5;
  } else {
    return (base * height) / 2;
  }
}

